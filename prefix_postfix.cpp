#include<iostream>

using namespace std;
int main(){
int value{5};


cout<<"value++ "<<value++<<endl;// 5 will be input
cout<<"value-- "<<value--<<endl;// 6 will be input
cout<<"++value "<<++value<<endl;//5 will be input
cout<<"--value "<<--value<<endl;//6 will be input

    return 0;
}